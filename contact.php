        function validation()
        {
            var name = document.user.name.value;
            var email = document.user.email.value;
            var subject = document.user.subject.value;
            var message = document.user.message.value;

            if(name.length==0)
            {
                alert('Please Enter Name');
                document.user.name.focus();
                return false;
            }
            if(name.length<2)
            {
                alert('Please Enter valid Name');
                document.user.name.focus();
                return false;
            }
            if(email.length==0)
            {
                alert('Please Enter Mail');
                document.user.email.focus();
                return false;
            }
            var mailformat = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/;
            if(!email.match(mailformat))
            {
                alert('Please Enter valid Mail');
                document.user.email.focus();
                return false;
            }
            if(subject.length==0)
            {
                alert('Please Enter Subject');
                document.user.subject.focus();
                return false;
            }
            if(message.length==0)
            {
                alert('Please Enter message');
                document.user.message.focus();
                return false;
            }
        }
